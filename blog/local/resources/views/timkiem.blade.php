    <script>
        // Login
        $('#frm-login').on('submit', function () {
            showLoading('#modal1');

            // Handle request
            $.ajax({
                url: '/user/login',
                type: 'POST',
                data: {
                    username: $('input[name="username"]').first().val(),
                    password: $('input[name="password"]').first().val(),
                    remember: $('input[name="remember"]').first().is(':checked')
                }
            }).done(function (result) {
                if (result.status) {
                    // Login success, refresh page
                    window.location.href = result.link;
                } else {
                    // Login fail, display error
                    $('#login-msg').html('<div class="alert alert-danger">' + result.error + '</div>');
                }

                hideLoading('#modal1');
            }).fail(function (error) {
                console.log(error);
                hideLoading('#modal1');
            });

            return false;
        });

        // Register
        $('#frm-register').on('submit', function () {
            if (!validateRegisterForm()) {
                return false;
            }

            showLoading('#modal1');

            // Handle request
            $.ajax({
                url: '/user/register',
                type: 'POST',
                data: {
                    email: $('input[name="email"]').first().val(),
                    user_pass: $('input[name="user_pass"]').first().val(),
                    fullname: $('input[name="fullname"]').first().val(),
                    phone: $('input[name="phone"]').first().val(),
                    'g-recaptcha-response': grecaptcha.getResponse()
                }
            }).done(function (result) {
                if (result.status) {
                    window.location.href = result.link;
                } else {
                    grecaptcha.reset();
                    $('#btnRegister').addClass('disabled');
                    // Register fail, display error
                    $('#signup-msg').html('<div class="alert alert-danger">' + result.error + '</div>');
                }
                hideLoading('#modal1');
            }).fail(function (error) {
                hideLoading('#modal1');
            });

            return false;
        });
    </script>



